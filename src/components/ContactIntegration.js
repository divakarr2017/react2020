export default class ContactIntegration extends React.Component {
  constructor(props) {
    super(props);
    this.submitForm = this.submitForm.bind(this);
    this.state = {
      status: ""
    };
  }

  render() {
    const { status } = this.state;
    
    return (
      <form  onSubmit={this.submitForm}
      action="https://formspree.io/xgekzjen"
      method="POST" >
      <TextField
        variant="outlined"
        margin="normal"
        required
        fullWidth
        id="email"
        label="Email Address"
        name="email"
        autoComplete="email"
        autoFocus
      />
      <TextField
        variant="outlined"
        margin="normal"
        required
        fullWidth
        name="message"
        label="message"
        type="text"
        id="text"
      />
      
      <Button
        type="submit"
        fullWidth
        variant="contained"
        color="primary"
        
      >
        Sign In
      </Button>
      {status === "SUCCESS" && <p>Thanks!</p>}
        {status === "ERROR" && <p>Ooops! There was an error.</p>}
    </form>
      
    );
  }

  submitForm(ev) {
    ev.preventDefault();
    const form = ev.target;
    const data = new FormData(form);
    const xhr = new XMLHttpRequest();
    xhr.open(form.method, form.action);
    xhr.setRequestHeader("Accept", "application/json");
    xhr.onreadystatechange = () => {
      if (xhr.readyState !== XMLHttpRequest.DONE) return;
      if (xhr.status === 200) {
        form.reset();
        this.setState({ status: "SUCCESS" });
      } else {
        this.setState({ status: "ERROR" });
      }
    };
    xhr.send(data);
  }
}